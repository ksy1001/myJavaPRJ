public class Code05_03 {
    public static void main(String[]args){
        int num = 200;

        if (num < 100) {
            System.out.println("100보다 작군요.");
            System.out.println("여기는 팩트다..");
        } else {
            System.out.println("100보다 크군요.");
            System.out.println("여기는 틀렸다..");
        }

        System.out.println("프로그램 끝");
    }
}
